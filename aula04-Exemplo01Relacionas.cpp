#include <iostream>
using namespace std;

int main() {
	
	
    int x, y;
    
    x = 10;
    y = 5;
    
    cout << "x: " << x << endl;
    cout << "y: " << y << endl << endl;
    
    // igualdade
	cout << "x == 10 ? " << (x == 10) << endl;
	cout << "x == y ? " << (x == y) << endl;
	
	
	// diferenï¿½a
	cout << "x != 10 ? " << (x != 10) << endl;
	cout << "x != y ? " << (x != y) << endl;

	
	// maior
	cout << "x >= y ? " << (x >= y) << endl;

	// menor
	cout << "x <= y ? " << (x <= y) << endl;
	
}
